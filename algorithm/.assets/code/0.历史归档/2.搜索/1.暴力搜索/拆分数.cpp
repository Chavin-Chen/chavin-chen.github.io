#include<cstdio>
int ax[1000],ay[1000],ap;
int main(){
    int k,x;
    while(scanf("%d",&k)!=-1){
        ap=0;
        for(int y=1;y<=2*k;y++){	//·ÅËõ·¶Î§
            x=(k!=y)?(k*y/(y-k)):y;
            if(x>0&&k*(x+y)==x*y){
                ax[ap]=x;
                ay[ap++]=y;
            }
        }
        printf("%d\n",ap);
        for(int i=0;i<ap;i++)
            printf("1/%d=1/%d+1/%d\n",k,ax[i],ay[i]);
    }
    return 0;
}
